        <h1 class="text-et-crimson text-center">Latest News & Research</h1>

        <p class="lead">We will keep you up to date with all the latest news and research about Mitochondrial Disease.</p>

        <p>Mitochondria are the life force inside us all, they are our power supply in our cells providing energy for them to fuel our organs.</p>

        <p>Mitochondrial Disease is a relatively newly diagnosed disease - first recognised in an adult in the 1960s and in the 1980s in children.</p>

        <p>1 in 200 babies in the UK are born with genetic changes which can cause Mitochondrial Disease, that's a baby born every 30 minutes that could be diagnosed with this fatal disease.</p>

        <p>Faulty Mitochondria have been linked to many other common diseases and disorders such as diabetes, Parkinson's Disease, Cancer and Autism, amongst others.</p>

        <p>Mitochondrial Disease can affect any organ of the body and at any age. Symptoms are extremely diverse and often progressive.</p>

        <p>They include: strokes and seizures, muscle weakness, gastrointestinal disorders, swallowing difficulties, cardiac disease, liver disease, blindness and deafness, and susceptibility to infections.</p>

        <p>There are very few effective treatments and no cure for Mitochondrial Disease at present, meaning that babies born with this disease have very little chance of survival.</p>

        <p>Great Ormond Street Hospital is at the forefront of Mitochondrial Disease with Professor Shamima Rahman leading the way in a bid to find better and less invasive diagnostic methods, effective treatments and a cure.</p>

