<h3>Message from Royal Brompton & Harefield Hospitals Charity</h3>

<p>On the 12th July 2016, we were humbled to meet Paul and Donna, who sadly lost their infant son Elliot to mitochondrial disease and childhood cardiomyopathy last year at Great Ormond Street Hospital. Ever since their tragic loss, Paul and Donna and the team at Elliot’s Touch have bravely and tirelessly raised funds towards research into the condition, so that in future, children can be given a stronger fighting chance at life.</p>

<p>After doing some of their own research, Paul and Donna came across the Royal Brompton Hospital’s very own Dr Sanjay Prasad, an expert in cardiomyopathy. With his fantastic team, Dr Prasad is working towards finding a simple and effective way to test for cardiomyopathy, so that children and adults with this condition can be diagnosed and treated earlier, or even avoid the need for medical intervention.</p>

<p>Elliot’s Touch were able to meet with Dr Prasad and his team, learn more about their vital work, and, through their ambitious and truly remarkable fundraising efforts, present a generous cheque for £36,000 to help further the team’s work.</p>

<p>Dr Prasad said of the meeting and donation: “We are very mindful of the journey Elliot’s Touch have been through and how much toil has gone into raising funds – physically and emotionally. We will bear this responsibility by doing the best work we can.</p>

<p>“Our goal is very simple – to embody Elliot’s inspiration and determination to stop any other child suffering in the way he did. ‘That he gave of his today so that they may have their tomorrow.’”</p>

<p>You can read more about our inspiring meeting on our website: <a href="http://bit.ly/29HvbgP">http://bit.ly/29HvbgP</a></p>

